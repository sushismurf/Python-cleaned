import math

def area( radius ):
    """Calculate the area of a circle
     Parameters:
     radius (float): radius of circle

     Returns:
     float: calculated area

     """

    return math.pi * radius**2

def circumference ( radius ):
    return math.pi * 2 * radius


    
    
    
